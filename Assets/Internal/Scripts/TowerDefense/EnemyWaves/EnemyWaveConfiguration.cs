using System.Collections.Generic;
using UnityEngine;

// Класс "Конфигурация волны врагов" - настройка одной волны в игре.
// Это как рецепт: какие враги, в каком количестве и как часто появляются.
public class EnemyWaveConfiguration : MonoBehaviour
{
    // Группы врагов, участвующие в волне

    // Список групп врагов в этой волне - ОСНОВНОЕ СОДЕРЖИМОЕ ВОЛНЫ!
    // Каждая группа - это тип врага + количество.
    // Пример волны:
    // 1. 5 обычных врагов
    // 2. 3 быстрых врага
    // 3. 1 босс
    // Всё это настраивается визуально в редакторе!
    [field: SerializeField]
    public List<EnemyGroupConfiguration> EnemyGroups { get; private set; } = new();

    // Интервал между появлением врагов (в секундах)

    // Интервал между появлением врагов ВО ВСЕЙ ВОЛНЕ (в секундах)
    // Например: если значение 4, то новый враг будет появляться каждые 4 секунды
    // [Range(0, 60*60)] ограничивает значение от 0 секунд до 1 часа (3600 секунд)
    // Это защита от случайного ввода очень больших чисел
    [Range(0, 60 * 60)]
    [field: SerializeField]
    public int SpawnInterval { get; private set; } = 4;

    // Интервал между появлением врагов на одной начальной точке маршрута
    // (в секундах)

    // Интервал между появлением врагов на ОДНОЙ точке спавна (в секундах)
    // Зачем это нужно? Если у вас несколько точек спавна, и с одной точки только что вышел враг,
    // следующему врагу придется подождать это время, прежде чем выйти с той же точки.
    // Это предотвращает "столпотворение" врагов в начале пути.
    [Range(0, 60 * 60)]
    [field: SerializeField]
    public int SpawnPointCooldown { get; private set; } = 2;
}
