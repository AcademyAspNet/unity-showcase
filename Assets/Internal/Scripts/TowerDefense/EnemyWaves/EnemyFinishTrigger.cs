using System.Collections.Generic;
using UnityEngine;
using UnityEngine.Events;

// Класс "Триггер финиша для врагов" - ловушка в конце пути.
// Когда враг заходит в эту зону, он считается дошедшим до цели.
public class EnemyFinishTrigger : MonoBehaviour
{
    // Список тегов, которые считаем врагами.
    // По умолчанию здесь тег "Enemy", но можно добавить "Boss", "FastEnemy" и т.д.
    // Это нужно, чтобы разные типы врагов могли доходить до финиша.
    [SerializeField]
    private List<string> _enemyTags = new()
    {
        "Enemy" // Стандартный тег для обычного врага
    };

    // Событие "Враг дошел до финиша".
    // С помощью этого можно:
    // 1. Воспроизвести звук "враг прошел"
    // 2. Показать эффект
    // 3. Обновить UI
    // Настраивается в Inspector!
    [SerializeField]
    private UnityEvent _onEnemyReachedFinish;

    // Этот метод срабатывает, когда ЛЮБОЙ объект с коллайдером входит в нашу зону-триггер
    private void OnTriggerEnter(Collider other)
    {
        // Проверяем каждый тег из нашего списка "кто считается врагом"
        foreach (string enemyTag in _enemyTags)
        {
            // Если у вошедшего объекта НЕ тот тег, что мы проверяем сейчас..
            if (!other.CompareTag(enemyTag))
                continue; // ...пропускаем и проверяем следующий тег из списка

            // ***СРАБАТЫВАНИЕ!***
            // Если мы здесь, значит объект - враг с подходящим тегом!

            // 1. Запускаем все связанные события (играем звук и т.д.)
            _onEnemyReachedFinish?.Invoke(); // ?. - защита от ошибок если событие пустое

            // 2. Уничтожаем врага, чтобы он не копился на сцене
            Destroy(other.gameObject);

            // Выходим из метода - враг уже обработан
            return;
        }

        // Если мы прошли весь список тегов и не нашли совпадения,
        // значит вошедший объект - не враг (например, пуля или декорация)
        // Ничего не происходит - просто игнорируем его.
    }
}
